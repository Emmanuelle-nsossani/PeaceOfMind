        <h1>La santé mentale : Qu’est-ce que c’est et comment la préserver ?</h1>
        <img id="article-thumb" src="public/images/last-article.png" alt="illustration article">
        <p>Environ 20% de la population, soit 13 millions de Français, souffrent de troubles psychiques tels que la dépression et l'anxiété. Les jeunes adultes entre 18 et 25 ans sont particulièrement affectés, avec une augmentation notable des symptômes anxieux et dépressifs depuis 2020. Ces symptômes proviennent d’une mauvaise santé mentale.</p>
        <h2>La “Santé mentale” ? Quésaco ?</h2>
        <p>La santé mentale désigne la santé psychologique d’une personne, soit son bien être.
            Ce n’est pas quelque chose de figé, plutôt une évolution perpétuelle de notre cerveau, en recherche d’un équilibre psychique, en fonction de nos expériences. Cette dernière permet de faire face aux différents problèmes de la vie, et fait partie intégrante de la santé : ”Il n’y a pas de santé mentale sans santé” nous dit l’OMS (Organisation Mondiale de la Santé).
        </p>
        <h2>Qu’est-ce qu’une “Bonne” santé mentale ?</h2>
        <p>Une personne dite en “bonne“ santé mentale ne désigne pas seulement une personne ne présentant pas de symptôme de problèmes mentaux, mais aussi une personne qui se sent bien dans sa peau.
        </p>
        <h2>Comment connaître sa santé mentale ?</h2>
        <p>Pour connaître sa santé mentale, il suffit de prendre un rendez-vous avec un professionnel de la santé mentale, ou de réaliser un test validé par un professionnel.
            Si vous êtes curieux, vous pouvez réaliser notre test, qui à été réalisé spécialement pour vous.
        </p>
        <h2>Comment préserver sa santé mentale ?</h2>
        <p>Les éléments clés à respecter pour préserver aux mieux sa santé mentale sont les points suivant :</p>
        <ul>
            <li><b>Parler à quelqu’un : </b>Des fois, on à juste besoin de parler à une personne pour se sentir mieux. Que ce soit l’un de nos parents, un membre de notre famille
            </li>
            <li><b> Prendre rendez-vous avec un professionnel :</b> Les meilleures personnes à qui parler resteront les docteurs et psychologues.</li>
            <li><b>Ne pas se surmener :</b> Entre les cours, le travail, la vie familiale et sociale, on peut assez vite se laisser surpasser par les évènements. Il faut prendre le temps de se reposer, autant son corps que son cerveau.</li>
            <li><b>Pratiquer une activité physique : </b>Faire un sport permet d’occuper ton cerveau, donc pas de pensée négative.</li>
        </ul>
